export const getTemplatesByCategory = (categoryId: string): SpreadsheetTemplate[] => {
  if (categoryId === 'all') return templates;
  return templates.filter((t) => t.category === categoryId);
};

export const getTemplateById = (id: string): SpreadsheetTemplate | undefined => {
  return templates.find((t) => t.id === id);
};

export const getPopularTemplates = (): SpreadsheetTemplate[] => {
  return templates.filter((t) => t.isPopular);
};

export const getNewTemplates = (): SpreadsheetTemplate[] => {
  return templates.filter((t) => t.isNew);
};
